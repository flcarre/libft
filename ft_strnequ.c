#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t i)
{
	int n;

	n = 0;
	if (s1 && s2)
	{
		while(s1[n] && s2[n] && i)
		{
			if (s1[n] != s2[n])
				return(0);
			n++;
			i--;
		}
	}
	return(1);
}
