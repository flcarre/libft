#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *ret;

	if ((ret = malloc((size + 1) * sizeof(char))) == 0)
		return (NULL);
	while (size)
		ret[size--] = '\0';
	ret[size] = '\0';
	return (ret);
}
