#include "libft.h"

void	ft_striter(char *str, void (*f)(char *))
{
	int n;
	int s;

	n = 0;
	s = 0;
	if (str && *f)
	{
		s = ft_strlen(str);
		while(s)
		{
			f(&str[n]);
			n++;
			s--;
		}
	}
}
