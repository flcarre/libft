#include "libft.h"

char	*ft_strnew(size_t n)
{
	char *ret;

	if((ret = malloc((n + 1) * sizeof(char))) == 0)
	 return(NULL);
	while(n)
		ret[n--] = '\0';
	ret[n] = '\0';
	return (ret);
}
